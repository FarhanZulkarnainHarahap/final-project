import express from "express";
import {
  getAllStoreProducts,
  getProductsByStore,
  createStoreProduct,
  updateStoreProduct,
  getStoreProductById,
  deleteStoreProduct,
} from "../controllers/storeProduct-controller.js";

const router = express.Router();

// Route to get all store products
router.get("/", getAllStoreProducts);

router.get("/:storeId/products", getProductsByStore);

// Route to get a store product by ID
router.get("/:id", getStoreProductById);

// Route to create a new store product
router.post("/", createStoreProduct);

// Route to update an existing store product
router.put("/:id", updateStoreProduct);

// Route to delete a store product
router.delete("/:id", deleteStoreProduct);

export default router;
